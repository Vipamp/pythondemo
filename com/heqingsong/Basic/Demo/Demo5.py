#    Number类型的四则运算

print(5 + 4)
print(5 - 1)
print(5 * 4)
print(5 / 3)  # 浮点除法
print(5 // 3)  # 整除
print(5 % 3)  # 取余数
print(5 ** 3)  # 乘方

a = 1 + 2j  # 直接赋值复数
b = complex(1, 3)  # 使用complex方法赋值复数
print(a * b)
